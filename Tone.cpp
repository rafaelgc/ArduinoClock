Tone::Tone(int buzzerPin){
	this->buzzerPin = buzzerPin;
	currentNote = -1;

	//Hago que los silencios sean m√°s cortos.
	for (unsigned int i = 0; i<SIZE; ++i){
		silence[i]/=1.5;
	}

	loop = false;
}

void Tone::play(bool loop){
	currentNote = 0;
	this->loop = loop;
	if (currentNote!=SIZE){
		tone(buzzerPin, notes[currentNote],duration[currentNote]);
		chrono.restart();
	}
}

void Tone::update(){
	if (currentNote!=-1){
		if (chrono.getElapsedTime()>duration[currentNote]+silence[currentNote]){
			++currentNote;
			if (currentNote==SIZE){
				if (loop){
					currentNote = 0;
				}
				else{
					currentNote = -1;
					return;
				}
			}

			tone(buzzerPin, notes[currentNote], duration[currentNote]);
			chrono.restart();
		}
	}
}

bool Tone::hasFinished(){
	return currentNote==-1;
}

void Tone::stop(){
	currentNote = -1;
}

bool Tone::isPlaying(){
	return currentNote>=0;
}